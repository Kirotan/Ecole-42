#include <stdio.h>

int	ft_isalnum(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122) || (c >= 48 && c <= 57))
		return (1);
	else
		return (0);
}

int	main(int argc, char *argv[])
{
	if (argc == 2)
		printf("%d\n", ft_isalnum(argv[1][0]));
	return (0);
}
